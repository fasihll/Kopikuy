    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed

        if (txt_kode.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "pilih data terlebih dahulu!!");
        }else{
            int total = Integer.parseInt(txt_price.getText()) * Integer.parseInt(txt_quantity.getText());
        Penjualan.AddRowToTable(new Object[] {

            txt_kode.getText(),
            txt_nama.getText(),
            txt_category.getText(),
            txt_price.getText(),
            txt_quantity.getText(),
            String.valueOf(total)
        });
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

    private void btn_tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tambahMouseClicked

        try {
            int total_item = 0;
            int total_harga = 0;
            for (int i = 0; i < Penjualan.jTable1.getRowCount(); i++){
                int amount = Integer.parseInt((String) Penjualan.jTable1.getValueAt(i, 4));
                int amount1 = Integer.parseInt((String) Penjualan.jTable1.getValueAt(i, 5));
                total_item += amount;
                total_harga += amount1;
            }
        
          Penjualan.txt_total_item.setText(String.valueOf(total_item));
          Penjualan.txt_total1.setText(String.valueOf(total_harga));
          Penjualan.txt_total2.setText(String.valueOf(total_harga));
          
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "gagal"+e.getMessage());
        }

    }//GEN-LAST:event_btn_tambahMouseClicked

