    private void btn_tambah_fakturActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambah_fakturActionPerformed
        try {

            String sql = "select max(cast(substring(transaction.no_faktur,3) AS UNSIGNED)) as last_faktur from transaction ORDER BY substring(transaction.no_faktur,3) DESC;";
            PreparedStatement ps = db.koneksidb().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            rs.next();

            if (rs.getString("last_faktur") == null) {
                txt_faktur.setText("TI"+1);
            }else{
                int increment = Integer.parseInt(rs.getString("last_faktur"))+1;
                txt_faktur.setText("TI"+Integer.toString(increment));
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "gagal mengmbil faktur"+e);
        }
    }//GEN-LAST:event_btn_tambah_fakturActionPerformed

