    public void table_makanan(String keyword){
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(20);
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No");
        tbl.addColumn("Kode");
        tbl.addColumn("Nama");
        tbl.addColumn("Category");
        tbl.addColumn("Harga");
        tbl.addColumn("Quantity");
        jTable1.setModel(tbl);
        try {
            if (keyword != "") {
                String sql = "SELECT *,foods.name as nama_makanan, category.name as nama_category FROM foods JOIN category ON foods.categoryID = category.categoryID WHERE foods.foodID  LIKE '%"+keyword+"%' OR foods.name LIKE '%"+keyword+"%' OR category.name LIKE '%"+keyword+"%' ";  
                 PreparedStatement ps = db.koneksidb().prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                int no = 1;
                while (rs.next()) {                
                tbl.addRow(new Object[] {
                    no++,
                    rs.getString("foodID"),
                    rs.getString("nama_makanan"),
                    rs.getString("nama_category"),
                    rs.getString("price"),
                    rs.getString("food_stock")
                });
                jTable1.setModel(tbl);
            }
            }else{
                String sql = "SELECT *,foods.name as nama_makanan, category.name as nama_category FROM foods JOIN category ON foods.categoryID = category.categoryID";   
                    PreparedStatement ps = db.koneksidb().prepareStatement(sql);
                    ResultSet rs = ps.executeQuery();
                    int no = 1;
                    while (rs.next()) {                
                    tbl.addRow(new Object[] {
                    no++,
                    rs.getString("foodID"),
                    rs.getString("nama_makanan"),
                    rs.getString("nama_category"),
                    rs.getString("price"),
                    rs.getString("food_stock")
                });
                jTable1.setModel(tbl);
            }
            }
            db.koneksidb().close();
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "query table makanan salah"+e);
        }
    }
